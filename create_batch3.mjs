  return `生成一张“${item.n}结构解剖信息图”，风格参考人体骨骼解剖图、植物解剖图和医学教学挂图。将${item.n}像解剖标本一样一层一层拆开，做成清晰的爆炸分解图、剖面图和局部放大图。

画面中心展示${item.n}从外部形态到内部结构的分层：${item.s}。每一层或每个关键组织都要被单独分离出来，悬浮排列，层次清楚，像人体解剖图那样一寸一寸拆开。

每个结构旁边都要有细致的引线标注区域，说明这部分是什么、有什么功能、和风味或使用价值有什么关系，例如保护作用、储藏作用、挥发性香气来源、辛香来源、主要食用/药食使用部位、干燥后变化、研磨后用途等。${item.e}

整体要有强烈的科学展示感和教学感，像博物馆里的植物解剖教学图或高级食品科学图谱。视觉风格精密、清楚、理性、有秩序，构图像学术信息图，强调结构分析和功能解释，不要只是好看。

采用高级自然纸张背景、细腻线描、真实材质、轻微水彩或科学插画质感。主色调围绕${item.c}，保持专业、高级、克制，像顶级食品科学研究机构制作的教学海报。

请让版面清晰分区，包括：整体外观、分层爆炸图、纵向/横向剖面剖析、局部纹理放大、干燥或加工状态、粉末/切片/常见使用形态、功能说明区。

不要厨房场景，不要多余装饰，不要人手，不要器皿，不要无关食材，不要只做艺术海报。重点是“逐层解剖 + 功能解释 + 科学图示”。`;
